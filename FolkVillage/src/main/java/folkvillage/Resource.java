package folkvillage;

/**
 * Holds all the resource types
 * @author sanho
 */
public enum Resource {
    
    /**
     * Gold acts as currency
     */
    GOLD,
    
    /**
     * Iron is needed for some buildings
     */
    IRON,
    
    /**
     * Wood is the basic construction material
     */
    WOOD,
    
    /**
     * Food is eaten by population
     */
    FOOD
    
}
